int draw_mono_img(u8 *buffer, u8 *img, int color, int x, int y, int w, int h) {
    int c, d, i, j, z, wc = w / 8;
    for (c = (h - 1), d = 0; c >= 0; c--, d++) {
        u8 *tmp_img = img + wc * c;
        for (i = 0; i < wc; i++)
            for (j = 7, z = 0; j >= 0; j--, z++)
                if (tmp_img[i] & (1 << j))
                    plot_pixel(buffer, x + z + i * 8, y + d, (u8) color);
    }
    return 0;
}


void vga_update_screen() {
    char *p = 0xA0000;

    u32 tmp_size = buffer_size, plane_size = (640 * 480) / 8, i;

    for (i = 0; i < 4; i++) {
        zlox_vga_set_plane(i);
        if (tmp_size <= plane_size) {
            memcpy(p, buffer, tmp_size);
            break;
        } else {
            memcpy(p, buffer, plane_size);
            tmp_size -= plane_size;
            buffer += plane_size;
        }
    }
}
