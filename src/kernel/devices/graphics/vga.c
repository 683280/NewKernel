void write_regs(unsigned char *regs) {
    unsigned i;

/* write MISCELLANEOUS reg */
    outb(0x3C2, *regs);
    regs++;
/* write SEQUENCER regs */
    for (i = 0; i < 5; i++) {
        outb(0x3C4, i);
        outb(0x3C5, *regs);
        regs++;
    }
/* unlock CRTC registers */
    outb(0x3D4, 0x03);
    outb(0x3D5, inb(0x3D5) | 0x80);
    outb(0x3D5, 0x11);
    outb(0x3D4, inb(0x3D5) & ~0x80);
/* make sure they remain unlocked */
    regs[0x03] |= 0x80;
    regs[0x11] &= ~0x80;
/* write CRTC regs */
    for (i = 0; i < 25; i++) {
        outb(0x3d4, i);
        outb(0x3d5, *regs);
        regs++;
    }
/* write GRAPHICS CONTROLLER regs */
    for (i = 0; i < 9; i++) {
        outb(0x3ce, i);
        outb(0x3cf, *regs);
        regs++;
    }
/* write ATTRIBUTE CONTROLLER regs */
    for (i = 0; i < 21; i++) {
        (void) inb(0x3da);
        outb(0x3C0, i);
        outb(0x3C0, *regs);
        regs++;
    }
/* lock 16-color palette and unblank display */
    (void) inb(0x3DA);
    outb(0x3C0, 0x20);
}

int plot_pixel(u8 *buffer, int x, int y, u8 c) {
    int pmask, p, plane_size = (640 * 480) / 8;
    u8 mask;
    int wd_in_bytes = 640 / 8;
    int off = wd_in_bytes * y + x / 8;
    x = (x & 7) * 1;
    mask = 0x80;
    __asm__ __volatile__("shrb %%cl, %%al" :"=a"(mask):"c"(x), "0"(mask));
    //mask = 0x80 >> x;
    pmask = 1;
    for (p = 0; p < 4; p++) {
        if (pmask & c)
            buffer[off] |= mask;
        else
            buffer[off] &= ~mask;
        pmask <<= 1;
        buffer += plane_size;
    }
    return 0;
}

static void zlox_vga_set_plane(u32 p) {
    u8 pmask;

    p &= 3;
    pmask = 1 << p;
/* set read plane */
    outb(0x3CE, 4);
    outb(0x3CF, p);
/* set write plane */
    outb(0x3C4, 2);
    outb(0x3C5, pmask);
}

