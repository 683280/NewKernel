int draw_rect(u8 *buffer, int color, int x, int y, int w, int h) {
    int wd_in_bytes;
    int off, i;

    int p, s, e, n, m, plane_size = (640 * 480) / 8, pmask, x_in_bytes;
    u8 extra;
    s = x % 8;
    e = (w >= 8) ? 0 : (8 - s - w);
    n = (w - (8 - s - e)) / 8;
    m = (w - (8 - s - e)) % 8;
    wd_in_bytes = 640 / 8;
    x_in_bytes = x / 8;
    pmask = 1;
    for (p = 0; p < 4; p++) {
        for (i = 0; i < h; i++) {
            off = wd_in_bytes * (y + i) + x_in_bytes;
            if (pmask & color) {
                extra = 0xff;
                __asm__ __volatile__ ("shrb %%cl, %%al" :"=a"(extra):"c"(s), "0"(extra));
                extra &= (0xff << e);
                buffer[off] |= extra;
                memset((buffer + off + 1), 0xff, n);
                if (m > 0) {
                    extra = 0x80;
                    __asm__ __volatile__ ("sarb %%cl, %%al" :"=a"(extra):"c"(m - 1), "0"(extra));
                    buffer[off + 1 + n] |= extra;
                }
            } else {
                extra = 0xff;
                __asm__ __volatile__ ("shrb %%cl, %%al" :"=a"(extra):"c"(s), "0"(extra));
                extra &= (0xff << e);
                buffer[off] &= ~extra;
                memset((buffer + off + 1), 0x0, n);
                if (m > 0) {
                    extra = 0x80;
                    __asm__ __volatile__ ("sarb %%cl, %%al" :"=a"(extra):"c"(m - 1), "0"(extra));
                    buffer[off + 1 + n] &= ~extra;
                }
            }
        }
        pmask <<= 1;
        buffer += plane_size;
    }
    return 0;
}
